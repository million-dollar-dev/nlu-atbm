CREATE TABLE Users (
    UserID INT IDENTITY(1,1) PRIMARY KEY,
    Username NVARCHAR(50) NOT NULL,
    Password NVARCHAR(255) NOT NULL,
    Email NVARCHAR(100) NOT NULL,
	PhoneNumber VARCHAR(20) NOT NULL,
    Address NVARCHAR(MAX) NOT NULL,
    Role NVARCHAR(20) NOT NULL,
    CONSTRAINT UC_Username UNIQUE (Username),
    CONSTRAINT UC_Email UNIQUE (Email)
);

-- Bảng lưu trữ thông tin danh mục sản phẩm
CREATE TABLE Categories (
    CategoryID INT IDENTITY(1,1) PRIMARY KEY,
    CategoryName NVARCHAR(50) NOT NULL
);

-- Bảng lưu trữ thông tin sản phẩm
CREATE TABLE Products (
    ProductID INT IDENTITY(1,1) PRIMARY KEY,
    ProductName NVARCHAR(255) NOT NULL,
    Description NVARCHAR(MAX),
    Price INT NOT NULL,
	Sale INT,
    CategoryID INT FOREIGN KEY REFERENCES Categories(CategoryID)
);

-- Bảng lưu trữ thông tin số lượng sản phẩm theo màu sắc và size
CREATE TABLE ProductInventory (
    InventoryID INT IDENTITY(1,1) PRIMARY KEY,
    ProductID INT FOREIGN KEY REFERENCES Products(ProductID),
    Size NVARCHAR(10) NOT NULL,
    Quantity INT NOT NULL,
    CONSTRAINT UC_ProductInventory UNIQUE (ProductID, Size)
);

-- Bảng lưu trữ thông tin hình ảnh sản phẩm
CREATE TABLE ProductImages (
    ImageID INT IDENTITY(1,1) PRIMARY KEY,
    ProductID INT FOREIGN KEY REFERENCES Products(ProductID),
    ImagePath NVARCHAR(MAX) NOT NULL
);

-- Bảng lưu trữ thông tin đơn đặt hàng
CREATE TABLE Orders (
    OrderID INT IDENTITY(1,1) PRIMARY KEY,
    UserID INT FOREIGN KEY REFERENCES Users(UserID),
    OrderDate DATETIME NOT NULL,
    TotalAmount INT NOT NULL
);

-- Bảng chi tiết đơn đặt hàng
CREATE TABLE OrderDetails (
    OrderDetailID INT IDENTITY(1,1) PRIMARY KEY,
    OrderID INT FOREIGN KEY REFERENCES Orders(OrderID),
    ProductID INT FOREIGN KEY REFERENCES Products(ProductID),
    Quantity INT NOT NULL,
    Price INT NOT NULL
);

CREATE TABLE Reviews (
    ReviewID INT IDENTITY(1,1) PRIMARY KEY,
    ProductID INT,
    UserID INT,
    Rating INT,
    Comment TEXT,
    ReviewDate DATETIME,
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID),
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

